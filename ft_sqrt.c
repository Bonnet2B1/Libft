#include "libft.h"

int	ft_sqrt(int nb)
{
	int	sqr;

	sqr = 0;
	while (nb != sqr * sqr && sqr < nb && sqr <= 46340)
		sqr++;
	if (nb == sqr * sqr)
		return (sqr);
	return (0);
}
